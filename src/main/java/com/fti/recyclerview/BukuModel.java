package com.fti.recyclerview;

public class BukuModel {
    private String judulBuku;
    private String hargaBuku;
    private int gambarBuku;
    private String sinopsisBuku;

    public String getSinopsisBuku() {
        return sinopsisBuku;
    }

    public void setSinopsisBuku(String sinopsisBuku) {
        this.sinopsisBuku = sinopsisBuku;
    }

    public String getHargaBuku() {
        return hargaBuku;
    }

    public void setHargaBuku(String hargaBuku) {
        this.hargaBuku = hargaBuku;
    }

    public String getJudulBuku() {
        return judulBuku;
    }

    public void setJudulBuku(String judulBuku) {
        this.judulBuku = judulBuku;
    }

    public int getGambarBuku() {
        return gambarBuku;
    }

    public void setGambarBuku(int gambarBuku) {
        this.gambarBuku = gambarBuku;
    }

}
